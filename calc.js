/******************************************************************************
 * Global Variables
 *****************************************************************************/
const ERR = "Err";
/*****************************************************************************/

/******************************************************************************
 * Enums
 *****************************************************************************/
const Operations = Object.freeze({
    '+': add,
    '-': subtract,
    '*': multiply,
    '/': divide,
});
/*****************************************************************************/

/******************************************************************************
 * Functions
 *****************************************************************************/
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    if (0 === b) {
        return ERR;
    }
    return a / b;
}

function operate(op1, op2, operator) {

    if (Operations[operator]) {
        return Operations[operator](op1, op2);
    }
    else {
        return ERR;
    }

}
/*****************************************************************************/

/******************************************************************************
 * Exports
 *****************************************************************************/
module.exports = {
    add,
    subtract,
    multiply,
    divide,
    operate,
  };
